const suma = (numero1, numero2) => {
    return numero1 + numero2;
}

const resta = (numero1, numero2) => {
    return numero1 - numero2;
}

const multiplicacion = (numero1, numero2) => {
    return numero1 * numero2;
}

const division = (numero1, numero2) => {
    return numero1 / numero2;
}





